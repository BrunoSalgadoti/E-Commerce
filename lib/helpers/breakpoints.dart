/// # Breakpoint constants for responsive layout (Folder: helpers).
///
/// These constants define the breakpoints used to adapt the layout
/// of the app on different screen sizes such as mobile devices, tablets
/// and high-resolution monitors.
///
/// - `mobileBreakpoint`: Breakpoint for mobile devices.
/// - `tabletBreakpoint`: Breakpoint for tablets.
/// - `staggeredBreakpoint`: Breakpoint for layouts with staggered design.
/// - `wildBreakpoint`: Breakpoint for high-resolution monitors.
const double mobileBreakpoint = 700;
const double tabletBreakpoint = 1240;
const double staggeredBreakpoint = 980;
const double wildBreakpoint = 1920;
